drop database F3;
/*DB作成*/
CREATE DATABASE F3;
USE F3;
/*テーブル作成*/


/*users*/
CREATE TABLE users (	
	user_id INT AUTO_INCREMENT,
	region_id INT NOT NULL,
	icon_id INT DEFAULT 1,
	degree_id INT DEFAULT 1,
	user_name VARCHAR(30),
	password VARCHAR(100),
	mail VARCHAR(100),
	PRIMARY KEY (user_id),
	UNIQUE (mail)
);	


/*calendar*/
CREATE TABLE calendar (	
	user_id INT,
	current DATE,
	PRIMARY KEY (user_id, current)
);	


/*スコアポイント*/

CREATE TABLE scorePoint (	
	user_id INT,
	score INT DEFAULT 0,
	point INT DEFAULT 0,
	PRIMARY KEY (user_id)
);	


/*iconStatus*/

CREATE TABLE iconStatus (	
	user_id INT,
	icon_id INT,
	PRIMARY KEY (user_id, icon_id)
);	


/*garbage_type*/

CREATE TABLE garbage_type (	
	region_id INT,
	region_date DATE,
	types VARCHAR(255),
	PRIMARY KEY (region_id, region_date, types)
);	


/*region*/
CREATE TABLE region (	
	region_id INT AUTO_INCREMENT,
	region_name VARCHAR(100) NOT NULL,
	link VARCHAR(255),
	PRIMARY KEY (region_id)
);	


/*icon*/


CREATE TABLE icon (	
	icon_id INT,
	icon_name VARCHAR(255) NOT NULL,
	price INT NOT NULL,
	PRIMARY KEY (icon_id)
);	


/*degree*/

CREATE TABLE degree (	
	degree_id INT AUTO_INCREMENT,
	degree_name VARCHAR(30),
	PRIMARY KEY (degree_id)
);	





